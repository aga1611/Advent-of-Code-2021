export const getDirection = (data: Array<{ direction: string, number: number }>) => {
    let depth = 0;
    let horizontal = 0;
    for (const movement of data) {
        if (movement.direction === "forward") {
            horizontal += movement.number;
        }
        if (movement.direction.includes("down")) {
            depth = movement.number + depth;
        }
        if (movement.direction.includes("up")) {
            depth -= movement.number;
        }
    }
    return horizontal * depth
}

export const getDirectionWithAim = (data: Array<{ direction: string, number: number }>) => {
    let depth = 0;
    let horizontal = 0;
    let aim = 0;
    for (const movement of data) {
        if (movement.direction === "forward") {
            horizontal += movement.number;
            depth = depth + aim * movement.number;
        }
        if (movement.direction.includes("down")) {
            aim += movement.number;
        }
        if (movement.direction.includes("up")) {
            aim -= movement.number;
        }
    }
    return horizontal * depth
}
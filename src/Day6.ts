export const getNumberOfFish = (data: number[], numberOfDays: number) => {
    let fish = data;
    let i = 0
    while (i < numberOfDays) {
        for (let j = 0; j < fish.length; j++) {
            if (fish[j] === 0) {
                fish[j] = 7;
                fish.push(9)
            }
            if (fish[j] !== 8 || fish[j] !== 6) {
                fish[j] = fish[j] - 1
            }
        }
        i++
    }
    return fish.length
}
